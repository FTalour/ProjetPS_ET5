    public S2APHA getS2apha() {
        if(OKpressed){
            Solveur second = null;
            switch(comboBoxSolveurs.getItemAt(comboBoxSolveurs.getSelectedIndex())){
                case "recuit":
                    second = new Recuit();
                    break;
                case "PHA":
                    PhaOptions optPha = new PhaOptions(this);
                    optPha.setLocationRelativeTo(null);
                    optPha.setVisible(true);
                    second = optPha.getPha();
                    break;
                case "SAA":
                    SaaOptions optSaa = new SaaOptions(this);
                    optSaa.setLocationRelativeTo(null);
                    optSaa.setVisible(true);
                    second = optSaa.getSaa();
                    break;
                default:
                    second = null;
                    break;
            }
            if(second == null){
                Afficheur.erreurDialog("Solveur secondaire incompatible");
                return null;
            }else{
                if(panelAvance.isVisible())
                    return new S2APHA(sliderEchantillon.getValue(), second, sliderVar.getValue(), sliderDet.getValue());
                else
                    return new S2APHA(sliderEchantillon.getValue(), second);
            }
        }
        return null;
    }
